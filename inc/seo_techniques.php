<p>Webmasters and SEO managers use a variety of techniques to get their websites to top search engine rankings and drive more organic traffic.  This process could also be described as <i>building relevance</i> to search queries.  This article provides an overview of some of these techniques.  There are both internal and external techniques to boost relevance including:</p>
<ul>
	<li>Keywords and key phrases<li/>
	<li>Website	architecture<li/>
	<li>Writing	relevant content</li>
</ul>
<h2><a name="_nftvgyhhhlqf"></a>
What is SEO?</h2>
<p><b>Search Engine Optimization </b>or SEO is the process of driving traffic to a website through increased <i>organic search engine results</i>.<sup><a class="sdfootnoteanc" name="sdfootnote1anc" href="#sdfootnote1sym"><sup>1</sup></a></sup>
 Organic results are unpaid web page listings which are most relevant to a search query.<sup><a class="sdfootnoteanc" name="sdfootnote2anc" href="#sdfootnote2sym"><sup>2</sup></a></sup></p>
<h2><a name="_la5jp5tnimjw"></a>
Internal vs External optimization</h2>
<h3></a>Internal Optimization</h3>
<p>Internal or <i>onsite optimization</i> are things done directly to the website and its content to optimize search engine rankings.  These include such things as writing fresh and relevant content, keywords and key phrases, URL structure and website architecture.<sup><a class="sdfootnoteanc" name="sdfootnote3anc" href="#sdfootnote3sym"><sup>3</sup></a></sup></p>
<h3></a>External Optimization</h3>
<p>External or <i>offsite optimization</i> are any actions taken outside of the website to improve a website’s relevance.  They include such things as link building, social media marketing and video SEO.<sup><a class="sdfootnoteanc" name="sdfootnote4anc" href="#sdfootnote4sym"><sup>4</sup></a></sup></p>
<h2>Keywords and keyphrases</h2>
<p>This is perhaps the most direct form of SEO as it basically involves trying to include words and phrases in the website content and markup which matches relevant search queries.  A search query is a word or group of words which a user submits to a search engine like Google to find relevant content.<sup><a class="sdfootnoteanc" name="sdfootnote5anc" href="#sdfootnote5sym"><sup>5</sup></a></sup> It is important to consider your audience when choosing keywords and keyphrases to include in your website.  This means researching what terms people search for when seeking out the sort of content your website offers.  This can be done with the aid of tools like <a href="https://search.google.com/search-console/about"><font><u>google search console </u></font></a>and <a href="https://neilpatel.com/blog/10-free-keyword-research-tools-to-help-plan-your-new-site/"><font><u>keyword generators</u></font></a>.  
</p>
<p>Keywords should be used in a targeted way.  This means:</p>
<ul>
	<li>
	Choosing keywords carefully, according to what potential customers are searching for.
	<li/>
	<li>Limiting the number of keywords used for each page.
	<li/>
	<li>
	Targeting a handful of relevant keywords on each page.
	</li>
</ul>
<p>
Researching keywords and phrases can help to determine what sort of content to include in your website.  Keywords should be included in the markup at strategic positions including headings, URLs and image alt attributes.<sup><a class="sdfootnoteanc" name="sdfootnote6anc" href="#sdfootnote6sym"><sup>6</sup></a></sup></p>
<h2><a name="_efm9itv4sz8a"></a>
Link Building</h2>
<p>Also known as <b>backlinking</b>, this is the practice of improving search engine rankings by building one way hyperlinks (or backlinks) from authoritative websites back to your own.<sup><a class="sdfootnoteanc" name="sdfootnote7anc" href="#sdfootnote7sym"><sup>7</sup></a></sup> The number of quality backlinks to a website is used by the ranking algorithms of search engines to help determine the relevance of a site or page to search queries.  Links from trusted, high quality websites improve the reputation and authority of that website and can significantly improve search rankings.<sup><a class="sdfootnoteanc" name="sdfootnote8anc" href="#sdfootnote8sym"><sup>8</sup></a></sup>
</p>
<p>Links are built by reaching out to the owners of other websites, or by sending referral traffic.</p>
<h2 class="western"><a name="_plr3wihp50o6"></a>Website architecture</h2>
<p>The structure of a website is important for SEO as it affects how search engine web crawlers find and index the pages of a website.  If all pages can be easily found from the home page, it makes it easier for bots to find those pages.  This can also improve the authority of the homepage by providing internal ‘backlinks’ to the homepage.  It also improves user experience by making it easier for users to navigate the website.  
</p>
<p>Keep the site structure simple and hierarchical.   Posts should be organised by category and the URL structure should also follow this pattern.  As a general rule, all pages in a website should be no more than 4 clicks away from the homepage.<sup><a class="sdfootnoteanc" name="sdfootnote9anc" href="#sdfootnote9sym"><sup>9</sup></a></sup>
</p>
